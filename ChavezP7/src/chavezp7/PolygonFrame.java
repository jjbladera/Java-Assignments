    private void update() {
        //set information for image in drawpanel
        drawPanel1.setRadius(radius);
        drawPanel1.setNumberOfSides(sides);
        drawPanel1.setCircle(circumscribe);
        drawPanel1.setCircleColor(circleColor);

        //set calculator values
        calculator.setNumberOfSides(sides);
        calculator.setRadius(radius);

        //set information to show result of calculations
        infoPanel1.setPolygonArea(calculator.getPolygonArea());
        infoPanel1.setPolyPerimeter(calculator.getPerimeter());
        infoPanel1.setCircleArea(calculator.getCircleArea());
        infoPanel1.setCircumference(calculator.getCircumference());
        infoPanel1.setShowCircle(circumscribe);
        
    }
        
    
